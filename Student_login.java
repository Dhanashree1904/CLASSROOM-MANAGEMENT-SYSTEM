    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if(s_id.getText().equals(""))
        {
            JOptionPane.showMessageDialog(this,"Please fill user ID");
        }
        else if(spass.getText().equals(""))
        {
            JOptionPane.showMessageDialog(this, "Please fill password");
        }
        try
            {
               
                MySQLConnection obj=new MySQLConnection();
                String name=s_id.getText();
                String pass=spass.getText();
                String q="select * from login where username='"+name+"'and pass='"+pass+"'";
                
                ResultSet rs=obj.stm.executeQuery(q);
                if(rs.next())
                {
                  Student_portal sp=new Student_portal();
                  sp.show();
                  
                  dispose();
                }
                else
                {
                    s_id.setText("");
                    spass.setText("");
                    JOptionPane.showMessageDialog(null,"You have entered Wrong Username and Password");   
                }
                
            }
            catch(Exception e)
            {
                e.printStackTrace();
            }
    }//GEN-LAST:event_jButton2ActionPerformed

